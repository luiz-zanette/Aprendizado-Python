#Lê um valor em metros e exibe ele em centímetros e milímetros
print('Vamos converter metros para centimetros e milimetros')
metros = float(input('Digite um valor em metros:'))
print('Este valor convertido em centímetros vale: {}cm' .format(metros*100))
print('Este valor convertido em milimetros vale: {}mm'.format(metros*1000))